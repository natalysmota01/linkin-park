				<p>Much of the nü-metal of the late-1990s and early-2000s staked its claim on being as oafish as possible, but Linkin Park were the nü-metal group you could bring home to mom — and still play while hanging out with your frat-bound friends — they were sensitive and smart, but they still yelled. They formed in 1996, in the Los Angeles suburb of Agoura Hills, around the core of high school friends Mike Shinoda, Brad Delson, and Rob Bourdon. Joe Hahn and Dave Farrell soon joined, as well as a singer named Mark Wakefield, who left by 1999, when the group was called Hybrid Theory. He was replaced by Chester Bennington, an Arizonan who'd been in a group called Grey Daze. Bennington's urgent style worked nicely with Shinoda's hip-hop-influenced vocals, and Hybrid Theory soon became Linkin Park. Their first album, fittingly, was titled after the band's prior moniker. Hybrid Theory (Number Two, 2000) was the best-selling album of 2001 selling nearly 5 million albums helped along by tireless road work. Shinoda oversaw Reanimation (Nubmer Two, 2002), a remix disc featuring several guest appearances (Black Thought of the Roots and Jonathan Davis of Korn, for two).</p><br>

				<p>Linkin Park's next proper album, Meteora (Number One, 2003), continued the fusillade, with help from four Number one Modern Rock hits: "Somewhere I Belong" (Number 32 pop),"Numb" (Number 11 Pop), "Breaking the Habit" (Number 20 Pop), and "Lying From You" (Number 58 Pop). A year later, the group was tapped to back up Jay-Z on Collision Course (Number One, 2004), a "mash-up" album featuring a mix of lyrics and music from both artists. But after a period where the band members worked on outside projects, Linkin Park faced uncertainty for its third album — nü-metal had essentially died as a movement by the late2000s. Nevertheless, the band's Minutes to Midnight, co-produced by Rick Rubin, was triumphantly received, debuting at Number One and selling over a half-million copies its first week.</p>
